
public class Date {
    private int j ; 
    private int m; 
    private int a;
    public Date(int j, int m, int a) {
        this.j = j;
        this.m = m;
        this.a = a;
    }
    public int getJ() {
        return j;
    }
    public void setJ(int j) {
        this.j = j;
    }
    public int getM() {
        return m;
    }
    public void setM(int m) {
        this.m = m;
    }
    public int getA() {
        return a;
    }
    public void setA(int a) {
        this.a = a;
    }
   
    
    
}
